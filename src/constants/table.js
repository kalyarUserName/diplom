export function getTasks(id) {
    let res = [];
    tasks.map((task) => {
        if (task.user == id)
            res.push(task);
    })
    return res;
}

export function getPercent(id) {
    let tasksCount = 0;
    let res = 0;
    let tasks = getTasks(id);
    tasks.map((task) => {
        tasksCount++;
        if (task.status === "done") {
            res += 2;
        }
        if (task.status === "edit") {
            res += 1;
        }
    })
    return (res / (2 * tasksCount) * 100).toFixed(1);
}

export function getCountSplitTask(id) {
    let res = {
        done: 0,
        cancel: 0,
        edit: 0,
        none: 0
    };
    let tasks = getTasks(id);
    tasks.map((task) => {
        switch (task.status) {
            case 'done':
                res.done++;
                break;
            case 'cancel':
                res.cancel++;
                break;
            case 'edit' :
                res.edit++;
                break;
            default:
                res.none++;
                break;
        }
    })
    return res;
}